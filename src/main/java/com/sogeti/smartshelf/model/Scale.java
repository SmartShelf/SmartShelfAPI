package com.sogeti.smartshelf.model;

/**
 *
 * @author fabdin
 */
public class Scale {
    
    String id;
    String weight;
    String itemId;

    public String getWeight() {
        return weight;
    }

    public void setWeight(String weight) {
        this.weight = weight;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getItemId() {
        return itemId;
    }

    public void setItemId(String itemId) {
        this.itemId = itemId;
    }
    
    
}
